create database shoppingmallX;
use shoppingmallX;

create table Supplier(
	Supplier_name varchar(10) NOT NULL,
	Primary Key(Supplier_name)
);

create table Brand(
	Brand_name varchar(10) NOT NULL,
	Primary key(Brand_name)
);

create table Produce(
	Supplier_name varchar(10) NOT NULL,
	Brand_name varchar(10)	NOT NULL,
	Primary Key(Supplier_name, Brand_name),
	Foreign key(Supplier_name) references Supplier(Supplier_name),
	Foreign key(Brand_name) references Brand(Brand_name)
);

create table Category(
	Category_ID varchar(10),
	Main_category varchar(20),
	Middle_category varchar(20),
	Small_category varchar(20),
	Primary Key(Category_ID)
);


create table Product(
	Product_code varchar(10) NOT NULL,
	Price INT,
	Unit INT,
	Inventory INT,
	Product_name varchar(20),
	Standard varchar(10),
	Brand_name varchar(10),
	Category_ID varchar(10),
	Primary Key(Product_code),
	Foreign key(Brand_name) references Brand(Brand_name),
	Foreign key(Category_ID) references Category(Category_ID)
);

create table Contains(
	Cart_code varchar(10),
	Product_code varchar(10) NOT NULL,
	Primary Key(Product_code,Cart_code),
	Foreign key(Product_code) references Product(Product_code)
);

